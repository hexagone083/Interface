    private void panelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelMouseClicked

        if (isRightMouseButton(evt)) {
            clickDroit(evt);
        } else if (isLeftMouseButton(evt)) {
            clickGauche(evt);
        }
    }//GEN-LAST:event_panelMouseClicked

    private void clickDroit(java.awt.event.MouseEvent evt) {

        switch (etat) {
            case Init:
                etat = Etat.Init;
                //Ne rien Faire
                initNombrePoints();
                break;
            case UnPoint:
                etat = Etat.UnPoint;
                //Ne rien Faire
                //n = n
                break;
            case Plusieurs:
                if (nombrePoints() > 2) {
                    etat = Etat.Plusieurs;
                    supprimerDernierPoint();
                    tracerPolyligneNoire();
                    tracerLigneTemporaire(evt.getPoint());
                    decrementNombrePoints();
                } else if (nombrePoints() <= 2) {
                    etat = Etat.UnPoint;
                    supprimerDernierPoint();
                    tracerPolyligneNoire();
                    tracerLigneTemporaire(evt.getPoint());
                    setValeurNombrePoint(1);
                }
                break;

            case Max:
                etat = Etat.Plusieurs;
                supprimerDernierPoint();
                tracerPolyligneNoire();
                tracerLigneTemporaire(evt.getPoint());
                decrementNombrePoints();
                break;

        }
    }

    private void clickGauche(java.awt.event.MouseEvent evt) {

        switch (etat) {
            case Init:
                etat = Etat.UnPoint;
                panel.ajouterPoint(evt.getPoint());
                incrementNombrePoints();
                break;
            case UnPoint:
                etat = Etat.Plusieurs;
                panel.ajouterPoint(evt.getPoint());
                tracerPolyligneNoire();
                incrementNombrePoints();
                break;
            case Plusieurs:
                if (nombrePoints() < valeur_maximum) {
                    etat = Etat.Plusieurs;
                    panel.ajouterPoint(evt.getPoint());
                    tracerPolyligneNoire();
                    incrementNombrePoints();
                } else if (nombrePoints() <= valeur_maximum) {
                    etat = Etat.Max;
                    panel.ajouterPoint(evt.getPoint());
                    tracerPolyligneNoire();
                    incrementNombrePoints();
                }
                break;
            case Max:
                etat = Etat.Max;
                //Rien
                //n = n;
                break;

        }
    }

    private void KeyPressEspace(java.awt.event.KeyEvent evt) {

        switch (etat) {
            case Init:
                etat = Etat.Init;
                //Ne rien faire
                initNombrePoints();
                break;
            case UnPoint:
                etat = Etat.Init;
                enregistrerPolyligne();
                tracerPolylignesRouge();
                initNombrePoints();
                break;
            case Plusieurs:
                etat = Etat.Init;
                enregistrerPolyligne();
                tracerPolylignesRouge();
                initNombrePoints();
                break;
            case Max:
                etat = Etat.Init;
                enregistrerPolyligne();
                tracerPolylignesRouge();
                initNombrePoints();
                break;

        }
    }

